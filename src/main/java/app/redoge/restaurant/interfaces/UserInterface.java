package app.redoge.restaurant.interfaces;

/**
 * The interface User interface.
 * He described the most important methods of the object of the User
 * @author Redoge
 * @version 1.0
 */
public interface UserInterface {

    /**
     * Gets username.
     * @return the username: String
     */
    String getUsername();

    /**
     * Gets email.
     * @return the email: String
     */
    String getEmail();

    /**
     * Gets id.
     * @return the id: int
     */
    int getId();

    /**
     * Gets role.
     * @return the role: int
     */
    int getRole();

}
